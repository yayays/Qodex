import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(scriptDir);

const rpcContractPath = join(repoRoot, 'contracts', 'core-rpc.json');
const configContractPath = join(repoRoot, 'contracts', 'config-contract.json');
const dtoContractPath = join(repoRoot, 'contracts', 'dto-contract.json');
const edgeGeneratedPath = join(
  repoRoot,
  'packages',
  'qodex-edge',
  'src',
  'generated',
  'core-rpc.ts',
);
const edgeGeneratedConfigPath = join(
  repoRoot,
  'packages',
  'qodex-edge',
  'src',
  'generated',
  'config-contract.ts',
);
const edgeGeneratedDtoPath = join(
  repoRoot,
  'packages',
  'qodex-edge',
  'src',
  'generated',
  'dto-contract.ts',
);

const rpcContract = JSON.parse(await readFile(rpcContractPath, 'utf8'));
const configContract = JSON.parse(await readFile(configContractPath, 'utf8'));
const dtoContract = JSON.parse(await readFile(dtoContractPath, 'utf8'));

const fileContents = `// Generated by scripts/generate-contract-artifacts.mjs
// Do not edit manually.

export const JSONRPC_VERSION = ${JSON.stringify(rpcContract.jsonrpcVersion)} as const;

export const CoreMethods = ${JSON.stringify(rpcContract.methods, null, 2)} as const;

export const CoreEvents = ${JSON.stringify(rpcContract.events, null, 2)} as const;
`;

const configFileContents = `// Generated by scripts/generate-contract-artifacts.mjs
// Do not edit manually.

export const BackendKinds = ${JSON.stringify(configContract.backendKinds, null, 2)} as const;

export type BackendKind = typeof BackendKinds[number];

export const ConfigLoaderDefaults = ${JSON.stringify(configContract.loaderDefaults, null, 2)} as const;
`;

const dtoFileContents = `// Generated by scripts/generate-contract-artifacts.mjs
// Do not edit manually.

export const DtoContract = ${JSON.stringify(dtoContract, null, 2)} as const;
`;

await mkdir(dirname(edgeGeneratedPath), { recursive: true });
await writeFile(edgeGeneratedPath, fileContents, 'utf8');
await writeFile(edgeGeneratedConfigPath, configFileContents, 'utf8');
await writeFile(edgeGeneratedDtoPath, dtoFileContents, 'utf8');
