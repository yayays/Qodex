function renderChannelSection(channel, values, repoRoot) {
  if (channel === 'qq') {
    const qqPluginPath = resolve(repoRoot, './packages/qodex-channel-qqbot/src/index.ts');
    return [
      '[channels.qq]',
      'enabled = true',
      `plugin = ${quoteTomlString(qqPluginPath)}`,
      'channel_id = "qqbot"',
      'account_id = "main-account"',
      '',
      '[channels.qq.config]',
      `app_id = ${quoteTomlString(values.appId)}`,
      `client_secret_file = ${quoteTomlString(values.clientSecretFile)}`,
      'markdown_support = false',
      'request_timeout_ms = 15000',
      '',
    ];
  }

  if (channel === 'wechat') {
    return [
      '[channels.wechat]',
      'enabled = true',
      'plugin = "builtin:wechat-openclaw-compat"',
      'channel_id = "wechat-openclaw-compat"',
      `account_id = ${quoteTomlString(WECHAT_ACCOUNT_ID)}`,
      '',
      '[channels.wechat.config]',
      'adapter_module = "builtin:tencent-wechat"',
      'default_platform = "webchat"',
      'api_base_url = "https://ilinkai.weixin.qq.com"',
      `state_dir = ${quoteTomlString(WECHAT_STATE_DIR)}`,
      `login_artifact_dir = ${quoteTomlString(WECHAT_LOGIN_ARTIFACT_DIR)}`,
      `qr_filename = ${quoteTomlString(WECHAT_QR_FILENAME)}`,
      'request_timeout_ms = 15000',
      'login_wait_timeout_ms = 480000',
      '',
    ];
  }
}

function quoteTomlString(value) {
  return JSON.stringify(value);
}

function resolveWorkspace(value) {
  if (isAbsolute(value)) {
    return value;
  }
  return resolve(process.env.INIT_CWD ?? process.cwd(), value);
}

function resolveOptionalPath(value) {
  if (!value) {
    return value;
  }
  if (isAbsolute(value)) {
    return value;
  }
  return resolve(process.env.INIT_CWD ?? process.cwd(), value);
}

function readBackendKind(value) {
  if (value === 'codex' || value === 'opencode') {
    return value;
  }
  throw new Error(`unsupported backend kind: ${value}`);
}

export function readChannel(value) {
  if (value === 'qq' || value === 'wechat') {
    return value;
  }
  throw new Error(`unsupported channel: ${value}`);
}

function hasFlag(name) {
  return process.argv.includes(name);
}

function buildPrimaryStartCommand(configPath, skipAppServer) {
  const parts = ['npm run host:qodex -- --config', shellEscape(configPath)];
  if (skipAppServer) {
    parts.push('--skip-app-server');
  }
  return parts.join(' ');
}

function shellEscape(value) {
  if (/^[A-Za-z0-9_./:@-]+$/.test(value)) {
    return value;
  }
  return JSON.stringify(value);
}

async function startQuickStack({ configPath, skipAppServer, channel }) {
  const children = [];
  let shuttingDown = false;

  const shutdown = () => {
    if (shuttingDown) {
      return;
    }
    shuttingDown = true;
    for (const child of [...children].reverse()) {
      if (!child.killed && child.exitCode === null) {
        child.kill('SIGTERM');
      }
    }
  };

  process.on('SIGINT', shutdown);
  process.on('SIGTERM', shutdown);

  const host = spawnManaged(process.platform === 'win32' ? 'npm.cmd' : 'npm', [
    'run',
    'host:qodex',
    '--',
    '--config',
    configPath,
    ...(skipAppServer ? ['--skip-app-server'] : []),
  ]);
  children.push(host);

  if (channel === 'wechat') {
    await runWechatQuickLoginFlow(configPath);
  }

  await new Promise((resolvePromise) => {
    for (const child of children) {
      child.on('exit', (code) => {
        if (!shuttingDown && code && code !== 0) {
          process.exitCode = code;
        }
        shutdown();
        resolvePromise();
      });
      child.on('error', () => {
        process.exitCode = 1;
        shutdown();
        resolvePromise();
      });
    }
  });
}

function spawnManaged(command, args) {
  return spawn(command, args, {
    cwd: process.cwd(),
    env: process.env,
    stdio: 'inherit',
  });
}

async function promptIfMissing(flag, fallback, label, interactive, options = { required: true }) {
  const existing = getArg(flag);
  if (existing) {
    return existing;
  }

  if (!interactive) {
    if (fallback !== undefined) {
      return fallback;
    }
    if (options.required === false) {
      return undefined;
    }
    throw new Error(`${label} is required; pass ${flag} in non-interactive mode`);
  }

  const rl = readline.createInterface({ input, output });
  try {
    const suffix = fallback !== undefined ? ` [${fallback}]` : options.required === false ? ' [optional]' : '';
    const answer = (await rl.question(`${label}${suffix}: `)).trim();
    if (answer) {
      return answer;
    }
    if (fallback !== undefined) {
      return fallback;
    }
    if (options.required === false) {
      return undefined;
    }
    throw new Error(`${label} is required`);
  } finally {
    rl.close();
  }
}

async function promptChoiceIfMissing(flag, fallback, choices, label, interactive) {
  const existing = getArg(flag);
  if (existing) {
    return existing;
  }

  if (!interactive) {
    return fallback;
  }

  const rl = readline.createInterface({ input, output });
  try {
    const answer = (
      await rl.question(`${label} (${choices.join('/')}) [${fallback}]: `)
    ).trim();
    return answer || fallback;
  } finally {
    rl.close();
  }
}

async function promptChannelIfMissing(flag, fallback, interactive) {
  const existing = getArg(flag);
  if (existing) {
    return existing;
  }

  if (!interactive) {
    return fallback;
  }

  const choices = [
    {
      value: 'qq',
      description: '接入一个 QQ 机器人，适合远程聊天驱动开发任务',
    },
    {
      value: 'wechat',
      description: '内置腾讯微信扫码接入，启动后输出二维码链接供手动扫码',
    },
  ];

  output.write('Channel type\n');
  for (const [index, choice] of choices.entries()) {
    const recommended = choice.value === fallback ? ' (default)' : '';
    output.write(`  ${index + 1}. ${choice.value}${recommended} - ${choice.description}\n`);
  }

  const rl = readline.createInterface({ input, output });
  try {
    const answer = (
      await rl.question(`Choose channel [${fallback}]: `)
    ).trim();
    if (!answer) {
      return fallback;
    }

    const byIndex = Number(answer);
    if (Number.isInteger(byIndex) && byIndex >= 1 && byIndex <= choices.length) {
      return choices[byIndex - 1].value;
    }

    return answer;
  } finally {
    rl.close();
  }
}

function resolveWechatQuickStartPaths(configPath) {
  const configDir = dirname(resolveInputPath(configPath));
  return {
    qrFilePath: resolve(configDir, WECHAT_LOGIN_ARTIFACT_DIR, WECHAT_QR_FILENAME),
    sessionFilePath: resolve(configDir, WECHAT_STATE_DIR, 'accounts', `${WECHAT_ACCOUNT_ID}.json`),
  };
}

async function runWechatQuickLoginFlow(configPath) {
  const paths = resolveWechatQuickStartPaths(configPath);

  printSection('WeChat Login');
  printCheck('ok', 'wechat qr artifact', paths.qrFilePath);
  printCheck('ok', 'wechat session state', paths.sessionFilePath);

  const existingSession = await readWechatSession(paths.sessionFilePath);
  if (existingSession?.token) {
    printCheck(
      'ok',
      'wechat session',
      'saved token already exists; Qodex should connect immediately unless the token is stale',
    );
    return;
  }

  const qrLink = await waitForWechatQrLink(paths.qrFilePath, WECHAT_QR_WAIT_TIMEOUT_MS);
  if (!qrLink) {
    printCheck(
      'warn',
      'wechat qr link',
      `not found within ${Math.round(WECHAT_QR_WAIT_TIMEOUT_MS / 1000)}s; check Qodex logs and ${paths.qrFilePath}`,
    );
    return;
  }

  printCheck('ok', 'wechat qr link', qrLink);
  output.write('Open the link in a browser, render the QR code there, and scan it with WeChat.\n');

  if (input.isTTY && output.isTTY) {
    const rl = readline.createInterface({ input, output });
    try {
      await rl.question('After the scan is confirmed in WeChat, press Enter to verify login: ');
    } finally {
      rl.close();
    }
  } else {
    printCheck(
      'warn',
      'wechat login confirmation',
      'non-interactive terminal; continuing to poll session state automatically',
    );
  }

  const confirmedSession = await waitForWechatConfirmedSession(
    paths.sessionFilePath,
    WECHAT_CONFIRM_WAIT_TIMEOUT_MS,
  );
  if (confirmedSession?.token) {
    printCheck('ok', 'wechat login verified', 'saved session token detected');
    return;
  }

  printCheck(
    'warn',
    'wechat login verified',
    `no saved session token detected within ${Math.round(WECHAT_CONFIRM_WAIT_TIMEOUT_MS / 1000)}s`,
  );
}

async function waitForWechatQrLink(qrFilePath, timeoutMs) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    const text = await readTrimmedFile(qrFilePath);
    if (text) {
      return text;
    }
    await sleep(250);
  }
  return undefined;
}

async function waitForWechatConfirmedSession(sessionFilePath, timeoutMs) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    const session = await readWechatSession(sessionFilePath);
    if (session?.token) {
      return session;
    }
    await sleep(500);
  }
  return undefined;
}

async function readWechatSession(sessionFilePath) {
  try {
    const raw = await readFile(sessionFilePath, 'utf8');
    return JSON.parse(raw);
  } catch {
    return undefined;
  }
}

async function readTrimmedFile(filePath) {
  try {
    const raw = await readFile(filePath, 'utf8');
    const trimmed = raw.trim();
    return trimmed.length > 0 ? trimmed : undefined;
  } catch {
    return undefined;
  }
}

async function sleep(ms) {
  await new Promise((resolvePromise) => setTimeout(resolvePromise, ms));
}

if (process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1])) {
  await main().catch((error) => {
    console.error(error);
    process.exitCode = 1;
  });
}
